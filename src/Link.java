public class Link {
    private long len;
    private long spd;

    public Link(long len, long spd){
        this.len = len;
        this.spd = spd;
    }
    
    public void setLink(long len, long spd){
        this.len = len;
        this.spd = spd;
    }

    public long getDelay(){
        return len/spd;
    }
}
